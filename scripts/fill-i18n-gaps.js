// Merge all together
const ALL = { ...TRANSLATIONS, ...SUB_PAGES, ...SHARED };

// Apply to each lang file
for (const lang of ['de', 'en', 'tr', 'ar']) {
  const filePath = path.join(REPO, 'messages', lang + '.json');
  const j = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  let added = 0;
  for (const ns in ALL) {
    if (!ALL[ns][lang]) continue;
    j[ns] = j[ns] || {};
    for (const k in ALL[ns][lang]) {
      if (!(k in j[ns])) {
        j[ns][k] = ALL[ns][lang][k];
        added++;
      }
    }
  }
  fs.writeFileSync(filePath, JSON.stringify(j, null, 2) + '\n');
  console.log(`✓ ${lang}: ${added} keys added`);
}
